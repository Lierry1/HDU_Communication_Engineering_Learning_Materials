%求系统的冲激响应序列
clf;
n=0:20;
x=cos(20*pi*n/256)+cos(200*pi*n/256);
x1=[x 0 0 0 0];
x2=[0 x 0 0 0];
x3=[0 0 x 0 0]; %x2=x(n)
x4=[0 0 0 x 0];
x5=[0 0 0 0 x];
y1=0.5.*x3+0.27.*x4+0.77.*x5;

num=[0.45 0.5 0.45];
den=[1 -0.53 0.46];
y2=filter(num,den,x);

b1=[0.5 0.27 0.77];
a1=[1 0 0];
b2=[0.45 0.5 0.45];
a2=[1 -0.53 0.46];
y3=impz(b1,a1,n);
y4=impz(b2,a2,n);

subplot(2,1,1)
stem(y3);
title('系统1');

subplot(2,1,2)
stem(y4);
title('系统2');
